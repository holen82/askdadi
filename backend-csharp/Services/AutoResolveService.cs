using System.Text.RegularExpressions;
using DadiChatBot.Models;

namespace DadiChatBot.Services;

public class AutoResolveService(
    GitHubService github,
    OpenAIService openAI,
    ILogger<AutoResolveService> logger)
{
    public async Task ProcessSingleIssueAsync(GitHubIssue issue, CancellationToken ct = default)
    {
        List<SourceFile> sourceFiles;
        try
        {
            sourceFiles = await FetchSourceFilesAsync(ct);
            logger.LogInformation("Fetched {Count} source file(s) for issue #{Number}.", sourceFiles.Count, issue.Number);
        }
        catch (Exception ex)
        {
            logger.LogError(ex, "Failed to fetch source files for issue #{Number}. Aborting.", issue.Number);
            return;
        }

        try
        {
            await ProcessIssueAsync(issue, sourceFiles, ct);
        }
        catch (Exception ex)
        {
            logger.LogError(ex, "Failed to auto-resolve issue #{Number}: {Title}", issue.Number, issue.Title);
        }
    }

    public async Task<int> RunAutoResolveAsync(CancellationToken ct = default)
    {
        var issues = await github.GetIssuesForAutoResolveAsync(ct);
        logger.LogInformation("Found {Count} issue(s) to auto-resolve.", issues.Count);

        if (issues.Count == 0) return 0;

        List<SourceFile> sourceFiles;
        try
        {
            sourceFiles = await FetchSourceFilesAsync(ct);
            logger.LogInformation("Fetched {Count} source file(s).", sourceFiles.Count);
        }
        catch (Exception ex)
        {
            logger.LogError(ex, "Failed to fetch source files. Aborting auto-resolve.");
            return 0;
        }

        int processed = 0;
        foreach (var issue in issues)
        {
            try
            {
                await ProcessIssueAsync(issue, sourceFiles, ct);
                processed++;
            }
            catch (Exception ex)
            {
                logger.LogError(ex, "Failed to process issue #{Number}: {Title}", issue.Number, issue.Title);
            }
        }
        return processed;
    }

    private async Task ProcessIssueAsync(GitHubIssue issue, List<SourceFile> sourceFiles, CancellationToken ct)
    {
        logger.LogInformation("Processing issue #{Number}: {Title}", issue.Number, issue.Title);

        // Step 1: Add autoresolve label first for idempotency
        await github.AddLabelToIssueAsync(issue.Number, "autoresolve", ct);

        // Step 2: Generate implementation plan
        string plan;
        try
        {
            plan = await openAI.GenerateImplementationPlanAsync(
                issue.Title,
                issue.Body ?? string.Empty,
                sourceFiles,
                ct);
        }
        catch (InvalidOperationException ex) when (ex.Message == "CONTEXT_LENGTH_EXCEEDED")
        {
            logger.LogWarning("Context length exceeded for issue #{Number}. Retrying with reduced context.", issue.Number);
            var reduced = GetReducedFiles(sourceFiles);
            plan = await openAI.GenerateImplementationPlanAsync(
                issue.Title,
                issue.Body ?? string.Empty,
                reduced,
                ct);
        }

        // Step 3: Post plan as comment
        var commentBody = $"## Auto-Resolution Plan\n\n{plan}\n\n---\n*Generated by DadiChatBot AutoResolve*";
        await github.PostIssueCommentAsync(issue.Number, commentBody, ct);

        // Step 4: Generate code changes
        AiCodeChangesResponse codeChanges;
        try
        {
            codeChanges = await openAI.GenerateCodeChangesAsync(
                issue.Title,
                issue.Body ?? string.Empty,
                plan,
                sourceFiles,
                ct);
        }
        catch (InvalidOperationException ex) when (ex.Message == "CONTEXT_LENGTH_EXCEEDED")
        {
            logger.LogWarning("Context length exceeded for code gen on issue #{Number}. Retrying with reduced context.", issue.Number);
            var reduced = GetReducedFiles(sourceFiles);
            codeChanges = await openAI.GenerateCodeChangesAsync(
                issue.Title,
                issue.Body ?? string.Empty,
                plan,
                reduced,
                ct);
        }

        if (codeChanges.FileChanges.Count == 0)
        {
            logger.LogWarning("AI returned no file changes for issue #{Number}. Skipping PR creation.", issue.Number);
            return;
        }

        // Create branch
        var headSha = await github.GetHeadShaAsync(ct);
        var branchName = BuildBranchName(issue);
        await github.CreateBranchAsync(branchName, headSha, ct);

        // Commit each file
        foreach (var (filePath, content) in codeChanges.FileChanges)
        {
            try
            {
                var existingSha = await github.GetFileShaAsync(filePath, branchName, ct);
                await github.UpsertFileAsync(
                    filePath,
                    content,
                    $"Auto-resolve #{issue.Number}: update {filePath}",
                    branchName,
                    existingSha,
                    ct);
            }
            catch (Exception ex)
            {
                logger.LogError(ex, "Failed to commit file {FilePath} for issue #{Number}", filePath, issue.Number);
            }
        }

        // Create PR
        var prTitle = $"Auto-resolve: #{issue.Number} - {issue.Title}";
        var prBody = $"Closes #{issue.Number}\n\n{codeChanges.Summary}\n\n*Auto-generated by DadiChatBot AutoResolve*";
        var prUrl = await github.CreatePullRequestAsync(prTitle, prBody, branchName, ct);
        logger.LogInformation("Created PR for issue #{Number}: {Url}", issue.Number, prUrl);
    }

    private async Task<List<SourceFile>> FetchSourceFilesAsync(CancellationToken ct)
    {
        var tree = await github.GetRepoTreeAsync(ct);
        var relevant = tree.Where(item => item.Type == "blob" && IsRelevantFile(item.Path)).ToList();

        var files = new List<SourceFile>();
        foreach (var item in relevant)
        {
            try
            {
                var content = await github.GetBlobContentAsync(item.Sha, ct);
                files.Add(new SourceFile(item.Path, content));
            }
            catch (Exception ex)
            {
                logger.LogWarning(ex, "Failed to fetch blob for {Path}", item.Path);
            }
        }
        return files;
    }

    private static bool IsRelevantFile(string path)
    {
        if (path.Contains("/bin/") || path.Contains("/obj/") ||
            path.Contains("node_modules/") || path.Contains("/dist/") ||
            path.Contains("/.vs/") || path.EndsWith(".css") || path.EndsWith(".d.ts"))
            return false;

        if (path.StartsWith("backend-csharp/", StringComparison.OrdinalIgnoreCase) &&
            (path.EndsWith(".cs") || path.EndsWith(".csproj")))
            return true;

        if (path.StartsWith("frontend/src/", StringComparison.OrdinalIgnoreCase) && path.EndsWith(".ts"))
            return true;

        if (path.Equals("frontend/index.html", StringComparison.OrdinalIgnoreCase))
            return true;

        return false;
    }

    private static List<SourceFile> GetReducedFiles(List<SourceFile> sourceFiles) =>
        sourceFiles
            .Where(f => f.Path.StartsWith("backend-csharp/Services/", StringComparison.OrdinalIgnoreCase)
                     || f.Path.StartsWith("backend-csharp/Functions/", StringComparison.OrdinalIgnoreCase))
            .ToList();

    private static string BuildBranchName(GitHubIssue issue)
    {
        var slug = Regex.Replace(issue.Title.ToLowerInvariant(), @"[^a-z0-9]+", "-").Trim('-');
        if (slug.Length > 40) slug = slug[..40].TrimEnd('-');
        return $"auto-resolve/issue-{issue.Number}-{slug}";
    }
}
